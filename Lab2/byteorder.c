int
main(int argc, char **argv)
{
	union {
	  short  s;
      char   c[sizeof(short)];
    } un;

	un.s = 0x0102;
	//printf("%s: ", CPU_VENDOR_OS);
	if (sizeof(short) == 2) {
                // Check the ordering of how the bytes in the short is stored, for big endian it will be left most first to right most last
		if (un.c[0] == 1 && un.c[1] == 2)
			printf("big-endian\n");
                // For little endian it will be right most first, left most last
		else if (un.c[0] == 2 && un.c[1] == 1)
			printf("little-endian\n");
		else
			printf("unknown\n");
	} else
		printf("sizeof(short) = %d\n", (int)sizeof(short));

	exit(0);
}
